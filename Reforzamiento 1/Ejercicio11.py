"""11. Identificar que tipo de dato se obtiene al elevar un número a la exponente de 5 y
luego dividirlo por 10. Mostrar el resultado en pantalla."""

var_1 = 5

potencia = var_1 ** 5

division = potencia / 10

print("El resultado de la operación es: {}".format(division))
print("El tipo de dato del resultado es: {}".format(type(division)))
