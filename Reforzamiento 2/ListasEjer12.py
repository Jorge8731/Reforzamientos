"""12. Reconocer los tipos de cada dato en la lista creadas y mostrarla en consola (type())"""

lista = [25, 29.53, "Hola Pythonistas", True]

print("El tipo del primer valor de la lista es: {}".format(type(lista[0])))
print("El tipo del segundo valor de la lista es: {}".format(type(lista[1])))
print("El tipo del tercer valor de la lista es: {}".format(type(lista[2])))
print("El tipo del cuarto valor de la lista es: {}".format(type(lista[3])))